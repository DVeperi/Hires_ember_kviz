def keres_hires_ember(nev):
    """OpenAI-val lekérdezi a ismert ember adatait."""
    prompt = f"""
A feladatod: egy ismert személy adatait kell visszaadnod.
A név lehet magyar vagy külföldi híresség is.
Kiemelten kezeld a magyar hírességeket, például:
- sportolók: Puskás Ferenc, Egerszegi Krisztina, Hosszú Katinka, Dzsudzsák Balázs
- zenészek: Freddie Mercury, Presser Gábor, Ákos, Korda György
- tudósok: Szent-Györgyi Albert, Neumann János, Teller Ede
- írók: Kertész Imre, Márai Sándor, József Attila
- közéleti szereplők: Rubik Ernő, Gábor Dénes
- Használd a Wikipedia adatait

    A felhasználó ezt a nevet adta meg: {nev}

    Add vissza az alábbi mezőket:
    - Név (helyes, teljes név)
    - Születési idő
    - Születési város
    - Születési ország
    - Foglalkozás / tevékenység (pl. fizikus, festő, színész, sportoló + sportág)

    Ha NEM létező vagy nem híres személyt adtak meg:
    VÁLASZ: "Nem találtam adatot az általad megadott személyről."

    Ne találj ki adatot, ne hallucinálj.
    """

    response = client.chat.completions.create(
        model="gpt-4o-mini",
        messages=[{"role": "user", "content": prompt}],
        temperature=0
    )

    return response.choices[0].message.content

