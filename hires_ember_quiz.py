def generalj_kviz_kerdesenkent():
    prompt = """
    Készíts egy 10 éves gyermekeknek szóló, 10 kérdésből álló híres ember kvízt.
    Legyenek benne 30%-ban magyar híres emberek és 70%-ban nem magyar híres emberek is.
     név lehet magyar vagy külföldi híresség is.
  Kiemelten kezeld a magyar hírességeket, például:
  -  sportolók: Puskás Ferenc, Egerszegi Krisztina, Hosszú Katinka, Dzsudzsák Balázs
  - zenészek: Freddie Mercury, Presser Gábor, Ákos, Korda György
  - tudósok: Szent-Györgyi Albert, Neumann János, Teller Ede
  - írók: Kertész Imre, Márai Sándor, József Attila
  - közéleti szereplők: Rubik Ernő, Gábor Dénes

    Minden kérdés:
    - Tartalmazza a híres ember nevét
    - A kérdés vagy a születési országra, vagy a foglalkozásra kérdezzen rá
    - Legyen pontosan 4 válaszlehetőség: 1) 2) 3) 4)
    - Add meg a helyes választ szám formájában (1 -4)

    A kimenet formátuma legyen:

    1. Kérdés szövege
    1) ...
    2) ...
    3) ...
    4) ...
    Helyes válasz: X

    (és így tovább 10 kérdésig)
    """

    response = client.chat.completions.create(
        model="gpt-4o-mini",
        messages=[{"role": "user", "content": prompt}],
        temperature=0.7
    )

    return response.choices[0].message.content

"""5. cella – Kvíz futtatása és értékelése"""

def futtasd_kviz_kerdesenkent():
    kviz = generalj_kviz_kerdesenkent()
    sorok = kviz.split("\n")

    kerdesek = []
    aktualis = []

    for sor in sorok:
        if sor.strip().startswith("Helyes válasz:"):
            aktualis.append(sor)
            kerdesek.append(aktualis)
            aktualis = []
        else:
            if sor.strip():
                aktualis.append(sor)

    pont = 0

    print("\n=== Híres ember kvíz ===\n")

    for i, kerdes in enumerate(kerdesek):
        print(f"\n--- {i+1}. kérdés ---")

        for sor in kerdes[:-1]:
            print(sor)

        helyes = kerdes[-1].split(":")[1].strip()

        valasz = None
        while valasz not in ["1", "2","3","4"]:
            valasz = input("Válasz (1 - 4): ").strip()

        if valasz == helyes:
            print("✔️ Helyes!")
            pont += 1
        else:
            print(f"❌ Nem jó. A helyes válasz: {helyes}")

    print("\n=== Eredmény ===")
    print(f"Pontszám: {pont}/10")

    if pont >= 9:
        print("Fantasztikus teljesítmény!")
    elif pont >= 6:
        print("Ügyes vagy!")
    elif pont >= 4:
        print("Ezt még gyakorolnod kell!")
    else:
        print("Csinálj inkább cigánykereket 😉!")

